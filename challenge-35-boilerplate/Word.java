public class Word {
    private String word;
    private int occurance;

    public Word(String word, int occurance) {
        this.word = word;
        this.occurance = occurance;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public int getOccurance() {
        return occurance;
    }

    public void setOccurance(int occurance) {
        this.occurance = occurance;
    }
    
}
